int	ft_strcmp(char *s1, char *s2)
{
	int d;

	d = 0;
	while (s1[d] == s2[d] && s1[d] != '\0' && s2[d] != '\0')
		d++;
	if (s1[d] == s2[d])
		return (0);
	else if (s1[d] > s2[d])
		return (1);
	else
		return (-1);
}
